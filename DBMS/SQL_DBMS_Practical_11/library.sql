CREATE TABLE books (
    book_id VARCHAR2 PRIMARY KEY,
    book_name VARCHAR2(100) NOT NULL,
    is_Borowed BOOLEAN,
    borowed_user_id 
);

CREATE TABLE Users (
    id NUMBER GENERATED by DEFAULT as IDENTITY,
    name VARCHAR2(100),
    email VARCHAR2(100),
    PRIMARY KEY (id),
    Book_id VARCHAR2(100),
    FOREIGN KEY (book_id) REFERENCES books(book_id)
);
